    private void btnConActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConActionPerformed
        loginModel obj = new loginModel();
        obj.setUsername(txtUser.getText());
        obj.setPassword(txtPass.getText());
        
        if (loginController.checkLogin(obj)) {
           loader.setVisible(true);
           login.setVisible(false);
            
           new java.util.Timer().schedule(new TimerTask() {
               @Override
                public void run() {
                mainView main2 = new mainView();
                main2.show();
                loader.setVisible(false);
                Notification notify = new Notification(main2, Notification.Type.SUCCESS, Notification.Location.TOP_RIGHT, "Login Successful!");
                notify.showNotification();
                dispose();
             } 
           }, 1000*3);
        } else {
            Notification notify = new Notification(this, Notification.Type.INFO, Notification.Location.TOP_RIGHT, "Username or Password is Incorrect!");
            notify.showNotification();
        }
    }//GEN-LAST:event_btnConActionPerformed

